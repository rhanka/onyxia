#!/usr/bin/env node
// Reads @sentropic/design-system-themes, maps it to the Onyxia palette shape,
// and emits a YAML fragment ready to splice under web.env in
// onyxia-private-values.local.yaml.
//
// Required env vars:
//   SENTROPIC_HEADER_LOGO_URL    e.g. https://cdn.sent-tech.ca/onyxia-logo.svg
//   SENTROPIC_HEADER_TEXT_BOLD   e.g. sent-tech
//   SENTROPIC_HEADER_TEXT_FOCUS  e.g. Datalab
// Optional env vars:
//   SENTROPIC_TAB_TITLE          default: "${BOLD} ${FOCUS} · Onyxia"
//   SENTROPIC_FAVICON_URL        default: SENTROPIC_HEADER_LOGO_URL
//   SENTROPIC_THEME_VERSION      default: v0.5.0 (used for jsdelivr URLs)
//   SENTROPIC_INJECT_FONT        default: false. When "true", emit the FONT
//                                block. Onyxia's env loader requires FONT.dirUrl
//                                to be a LOCAL path (starts with "/") unless
//                                the page is served from a kc context, so the
//                                default off keeps the v0.5.0 jsdelivr URL
//                                from crashing the SPA. See README "Brand it"
//                                for the v2 plan (init-container that pulls
//                                the woff2 files into the onyxia-web pod).
//
// Stdout: a YAML fragment with 2-space indentation, ready to inject under
// `web.env:` in the values file. The Onyxia chart already maps web.env.<KEY>
// to a container env var of the same name.

import { toOnyxiaPalettes, toOnyxiaFont } from './lib/mapping.mjs';
import { json5Stringify } from './lib/json5-stringify.mjs';
import { fileURLToPath } from 'node:url';

function requireEnv(name, env) {
  const v = env[name];
  if (!v || v.trim() === '') {
    const err = new Error(`${name} is required (export it before running the generator).`);
    err.exitCode = 2;
    throw err;
  }
  return v;
}

export async function generateWebEnv({
  env = process.env,
  importThemeModule = () => import('@sentropic/design-system-themes')
} = {}) {
  const HEADER_LOGO         = requireEnv('SENTROPIC_HEADER_LOGO_URL', env);
  const HEADER_TEXT_BOLD    = requireEnv('SENTROPIC_HEADER_TEXT_BOLD', env);
  const HEADER_TEXT_FOCUS   = requireEnv('SENTROPIC_HEADER_TEXT_FOCUS', env);
  const TAB_TITLE   = env.SENTROPIC_TAB_TITLE
    || `${HEADER_TEXT_BOLD} ${HEADER_TEXT_FOCUS} · Onyxia`;
  const FAVICON     = env.SENTROPIC_FAVICON_URL || HEADER_LOGO;
  const VERSION     = env.SENTROPIC_THEME_VERSION || 'v0.5.0';
  const INJECT_FONT = env.SENTROPIC_INJECT_FONT === 'true';

  const mod = await importThemeModule();
  const theme = mod.sentTechTheme || mod.default?.sentTechTheme;
  if (!theme) {
    const err = new Error(
      `@sentropic/design-system-themes did not export sentTechTheme. Exports: ${Object.keys(mod).join(', ')}`
    );
    err.exitCode = 3;
    throw err;
  }

  const { light, dark } = toOnyxiaPalettes(theme);
  assertPalette('light', light);
  assertPalette('dark', dark);

  // YAML scalar with embedded JSON5 — use the literal-block style `|-` so we
  // don't have to escape anything. envsubst (Task 5) treats it as opaque.
  const lines = [
    '# Auto-generated by helm-chart/examples/gke-ephemeral/theme/sentropic-to-onyxia.mjs',
    '# DO NOT EDIT BY HAND — re-run the generator after bumping @sentropic/design-system-themes.',
    `PALETTE_OVERRIDE_LIGHT: |-\n${indent(json5Stringify(light), 2)}`,
    `PALETTE_OVERRIDE_DARK: |-\n${indent(json5Stringify(dark), 2)}`
  ];

  if (INJECT_FONT) {
    const fontFoundation = mod.foundation?.font || mod.default?.foundation?.font;
    if (!fontFoundation) {
      const err = new Error(
        `@sentropic/design-system-themes did not export foundation.font. Exports: ${Object.keys(mod).join(', ')}`
      );
      err.exitCode = 3;
      throw err;
    }
    const font = toOnyxiaFont(fontFoundation, { version: VERSION });
    lines.push(`FONT: |-\n${indent(json5Stringify(font), 2)}`);
  } else {
    // Skipping FONT keeps Onyxia on its built-in font. ensureUrlIsSafe rejects
    // any non-"/" dirUrl when the SPA is served outside a kc context, so the
    // jsdelivr URL crashed the app. Flip SENTROPIC_INJECT_FONT=true only once
    // the fonts are served from a local path under the onyxia-web origin.
    lines.push('# FONT skipped — set SENTROPIC_INJECT_FONT=true once fonts are hosted locally.');
  }

  lines.push(
    `HEADER_LOGO: ${yamlString(HEADER_LOGO)}`,
    `HEADER_TEXT_BOLD: ${yamlString(HEADER_TEXT_BOLD)}`,
    `HEADER_TEXT_FOCUS: ${yamlString(HEADER_TEXT_FOCUS)}`,
    `TAB_TITLE: ${yamlString(TAB_TITLE)}`,
    `FAVICON: ${yamlString(FAVICON)}`
  );
  return lines.join('\n') + '\n';
}

function indent(s, n) {
  const pad = ' '.repeat(n);
  return s.split('\n').map(l => pad + l).join('\n');
}

function yamlString(s) {
  // Double-quoted scalar with backslash escapes — safe for URLs and spaces.
  return `"${s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

function assertPalette(name, p) {
  const required = [
    'focus.main', 'focus.light', 'focus.light2',
    'light.main', 'dark.main',
    'redError.main', 'greenSuccess.main', 'orangeWarning.main', 'blueInfo.main'
  ];
  for (const path of required) {
    const [a, b] = path.split('.');
    if (!p[a] || !p[a][b]) {
      const err = new Error(`${name} palette is missing ${path} - mapping or sentropic schema drift.`);
      err.exitCode = 4;
      throw err;
    }
  }
}

async function main() {
  const output = await generateWebEnv();
  process.stdout.write(output);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch(err => {
    console.error('ERROR:', err.message);
    process.exit(err.exitCode ?? 1);
  });
}
